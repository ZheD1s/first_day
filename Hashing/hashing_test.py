# Хеширование - это процесс превращения данных (строк, файлов) в короткую фиксированную последовательность символов
# (хеш), которая однозначно соответствует исходным данным, но не позволяет их восстановить
import hashlib

text = input("Введите текст: ")

md5_hash = hashlib.md5(text.encode()).hexdigest()
#   text.encode()
# Преобразует строку text в байты, потому что hashlib работает только с байтами, а не с обычными строками.
# По умолчанию используется кодировка UTF-8.
#
#   hashlib.md5(...)
# Создаёт объект хеша MD5 из переданных байтов.
# MD5 — алгоритм, который выдаёт хеш длиной 32 символа (128 бит).
#   .hexdigest()
# Преобразует хеш в читаемую строку в шестнадцатеричном формате.
# Например, "d41d8cd98f00b204e9800998ecf8427e".
# В итоге переменная md5_hash содержит строку с MD5-хешем введённого текста.

sha_hash = hashlib.sha256(text.encode()).hexdigest()
# Полностью аналогично MD5, но используется алгоритм SHA256.
# SHA256 — более безопасный алгоритм хеширования, чем MD5.
# Длина хеша: 64 символа (256 бит).
# В итоге sha_hash хранит SHA256-хеш введённого текста.

print("MD5:", md5_hash)
print("SHA256:", sha_hash)

# Программа берёт любой текст, хеширует его двумя алгоритмами (MD5 и SHA256) и выводит результат.
# MD5 быстрее, но менее безопасен (можно столкнуться с коллизиями).
# SHA256 более надёжен и часто используется для паролей, цифровых подписей и проверки целостности данных.